    public EliminarCoche(User u, VentanaPrincipal vp, Connection conn) {
        initComponents();
        this.vp = vp;
        this.conn = conn;
        DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();
        modelo.setRowCount(0);
        try {
            PreparedStatement ps = conn.prepareStatement("SELECT matricula,modelo,color FROM coches WHERE email = (SELECT email FROM login WHERE usuario = ?)");
            ps.setString(1, u.getNombre());
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                modelo.addRow(new Object[]{
                    rs.getString("matricula"),
                    rs.getString("modelo"),
                    rs.getString("color")
                });
            }
            rs.close();
            ps.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error con la base de datos", "ERROR SQL", JOptionPane.ERROR_MESSAGE);
        }
    }

