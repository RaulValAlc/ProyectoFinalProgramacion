    private void eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarActionPerformed
        int confirmacion = JOptionPane.showConfirmDialog(null, "¿Está seguro de que desea borrar el coche y su anuncio definitivamente?", "Confirmación", JOptionPane.YES_NO_OPTION);
        if (confirmacion == JOptionPane.YES_OPTION) {
            int fila = tabla.getSelectedRow();
            if (fila != -1) {
                try {
                    PreparedStatement ps = conn.prepareStatement("DELETE FROM coches WHERE matricula = ?");
                    String mat = tabla.getValueAt(fila, 0).toString();
                    ps.setString(1, mat);
                    ps.executeUpdate();
                    ps.close();
                    DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();
                    modelo.removeRow(fila);
                    JOptionPane.showMessageDialog(null, "Anuncio eliminado con éxito", "Operación exitosa", JOptionPane.INFORMATION_MESSAGE);
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Error con la base de datos", "ERROR SQL", JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Operación Cancelada", "Operación Cancelada", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_eliminarActionPerformed

